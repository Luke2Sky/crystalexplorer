ColorMapName colorMapFromString(const QString &s) {
    const auto &maps = availableColorMaps();
    for(int i = 0; i < maps.size(); i++) {
        if(s == colorMapToString(maps[i])) return maps[i];
    }
    return ColorMapName::CE_None;
}

